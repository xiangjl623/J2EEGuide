package com.glodon.num01.inheritance.exercise;

/**
 * 船（基类）
 * 
 * @author fucy
 * @date 2020年7月26日
 * @since JDK1.8
 */
public class Ship {
	
	public void left() {
		System.out.println("船向左运行...");
	}
	
	public void right() {
		System.out.println("船向右运行...");
	}
	
	public void forward() {
		System.out.println("船向前运行...");
	}
	
	public void back() {
		System.out.println("船向后运行...");
	}
	
}
